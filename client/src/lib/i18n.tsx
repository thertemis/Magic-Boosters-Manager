type Lang = keyof typeof translations;
type TranslationKey = keyof typeof translations.en;

const I18nContext = createContext<{ t: (key: TranslationKey) => string; lang: Lang }>({
  t: (key) => key,
  lang: "en",
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const { user } = useAuth();
  const lang: Lang = (user?.preferredLanguage as Lang) === "fr" ? "fr" : "en";

  const t = useMemo(() => {
    return (key: TranslationKey): string => {
      return (translations[lang] as any)[key] ?? (translations.en as any)[key] ?? key;
    };
  }, [lang]);

  return <I18nContext.Provider value={{ t, lang }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
